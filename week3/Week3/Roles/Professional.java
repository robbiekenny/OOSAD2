package Week3.Roles;

public abstract class Professional {

	private String name;
	private int age;

	public abstract void printDetails();

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return this.age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
